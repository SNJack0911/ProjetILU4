    @Override
    protected void paintComponent(Graphics g){
        super.paintComponent(g);
        Graphics2D g2d = (Graphics2D)g;
        
        // Draw the background image.
        if(isHover && hoverIcon != null){    
            g2d.drawImage(hoverIcon, 0, 0, getWidth(), getHeight(), this);
        } else if(defaultIcon != null ){
            g2d.drawImage(defaultIcon, 0, 0, getWidth(), getHeight(), this);
        }
    }
    
    private void setImage(){
        try{
                defaultIcon = ImageIO.read(new File("src/main/ressources/PiocheNormal.png"));
                hoverIcon = ImageIO.read(new File("src/main/ressources/PiocheHover.png"));
            } catch (IOException e){
                String userDirectory = new File("").getAbsolutePath();
                System.out.print("Pioche not found : " + userDirectory);
            }
    }

